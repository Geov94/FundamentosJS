var signo = prompt('Cual es tu signo ?')


switch (signo){

    case 'virgo' :

    console.log ('Necesitas un tiempo a solas. Pensar, analizar y reflexionar debe convertirse en la base para las decisiones que tomes en la vida. Animal del día: lobo.')
    break;

    case 'libra' :

    console.log ('La actitud que tomas en ocasiones no es la ideal para que los demás te tengan en cuenta. Sonríe, relájate y sé positivo. Animal del día: caballo de mar.')
    break;

    case 'escorpion' :

    console.log ('Habrá encuentros especiales con esa persona que marcó tu vida. Aprovecha la oportunidad de volver a empezar. No te arrepentirás. Animal del día: conejo.')
    break;
    case 'sagitario' :

    console.log ('Estás muy impaciente. Tienes una llamada pendiente que debes hacer muy pronto. No le des más largas al asunto.')
    break;
    case 'capricornio' :

    console.log ('Aventurarse hace parte de vivir. Toma riesgos y atrévete a experimentar lo que quieres descubrir, sin pedirle permiso a nadie más.')
    break;
    case 'acuario' :

    console.log ('No es un día para cometer excesos. Esto aplica para todos los aspectos de tu vida. Aprende a manejar las cosas con calma.')
    break;

    case 'piscis' :

    console.log ('Deja de poner a prueba a tu pareja, no hay necesidad de buscarle una caída. Tu imaginación te lleva al límite. ¿Quieres revivir el pasado?')
    break;

    case 'aries' :

    console.log ('Los reencuentros con las personas que quieres te llenan de energía. No olvides que tu familia es fundamental para que seas feliz.')
    break;

    case 'tauro' :

    console.log ('Estás dejando a un lado tus verdaderos ideales, por eso sientes que estás estancado y no avanzas. Tienes que hacer las cosas por ti mismo.')
    break;
    case 'geminis' :

    console.log ('Debes parar. Estás viviendo la vida a toda velocidad, sin detenerte a mirar nada de lo que pasa a tu alrededor. Te sorprenderías si lo hicieras.')
    break;
    case 'cancer' :

    console.log ('Deja de ir en contra del sistema. Tu capacidad de liderazgo y proyección está en ceros. El negativismo te está rodeando y te impide el éxito.')
    break;
    case 'leo' :

    console.log ('Aprovecha este buen momento para contagiar a los demás de tu energía. Hay augurio de muy buenos tiempos emocionales.')
    break;


    default:

    console.log('no digitaste bien')


  










}
